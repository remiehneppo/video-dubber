def _detect_calculus_spans(text: str) -> list[ProtectedSpan]:
    spans: list[ProtectedSpan] = []
    specs = [
        (r"dy\s*/\s*dx", "dy/dx", "d y trên d x", "dy/dx", "calculus_notation", "calculus.dy_dx"),
        (r"d\s*/\s*dx", "d/dx", "d trên d x", "d/dx", "calculus_notation", "calculus.d_dx"),
        (r"d\s*/\s*dt", "d/dt", "d trên d t", "d/dt", "calculus_notation", "calculus.d_dt"),
        (r"\bdx\s+squared\b", "dx²", "d x bình phương", "dx²", "calculus_notation", "calculus.dx_squared_words"),
        (r"π\s*r\s*(?:²|\^2)", "πr²", "pi r bình phương", "πr²", "formula", "calculus.pi_r_squared"),
        (r"\bpi\s+r\s+squared\b", "πr²", "pi r bình phương", "πr²", "formula", "calculus.pi_r_squared_words"),
        (r"½|\b1\s*/\s*2\b|\bone\s+half\b", "½", "một phần hai", "½", "fraction", "calculus.one_half"),
    ]
    for pattern, canonical, spoken, display, kind, rule_id in specs:
        for match in re.finditer(pattern, text, flags=re.IGNORECASE):
            spans.append(_span(match, kind, canonical, spoken, display, rule_id))
    for token in ("dr", "dx", "dy", "dt"):
        for match in re.finditer(_WORD_BOUNDARY.format(token=re.escape(token)), text, flags=re.IGNORECASE):
            forbidden = ("doctor", "bác sĩ", "tiến sĩ") if token == "dr" else ()
            spans.append(_span(match, "calculus_notation", token, f"d {token[1]}", token, f"calculus.{token}", forbidden))
    for match in re.finditer(r"(?<![A-Za-zÀ-ỹ])([A-Za-z])\s+of\s+([A-Za-z])\s+squared\b", text, flags=re.IGNORECASE):
        function_name = match.group(1)
        variable = match.group(2)
        canonical = f"{function_name}({variable})²"
        spans.append(_span(match, "power", canonical, f"{function_name}({variable}) bình phương", canonical, "calculus.function_squared_words"))
    for match in re.finditer(r"(?<![A-Za-zÀ-ỹ])([A-Za-z])(?:²|\^2)(?![A-Za-zÀ-ỹ])", text):
        variable = match.group(1)
        spans.append(_span(match, "power", f"{variable}²", f"{variable} bình phương", f"{variable}²", "calculus.variable_squared"))
    for match in re.finditer(r"(?<![A-Za-zÀ-ỹ])([A-Za-z])\s+squared\b", text, flags=re.IGNORECASE):
        variable = match.group(1)
        spans.append(_span(match, "power", f"{variable}²", f"{variable} bình phương", f"{variable}²", "calculus.variable_squared_words"))
    return spans


def _span(
    match: re.Match[str],
    kind: str,
    canonical: str,
    spoken: str,
    display: str,
    rule_id: str,
    forbidden: tuple[str, ...] = (),
) -> ProtectedSpan:
    return ProtectedSpan(
        kind=kind,
        source_text=match.group(0),
        canonical=canonical,
        spoken=spoken,
        display=display,
        start_char=match.start(),
        end_char=match.end(),
        confidence=1.0,
        rule_id=rule_id,
        forbidden=forbidden,
    )


def _dedupe_spans(spans: list[ProtectedSpan]) -> list[ProtectedSpan]:
    ordered = sorted(spans, key=lambda span: (span.start_char, -(span.end_char - span.start_char)))
    result: list[ProtectedSpan] = []
    occupied: list[tuple[int, int]] = []
    for span in ordered:
        if any(not (span.end_char <= start or span.start_char >= end) for start, end in occupied):
            continue
        result.append(span)
        occupied.append((span.start_char, span.end_char))
    return sorted(result, key=lambda span: (span.start_char, span.end_char))


def _safe_id(value: str) -> str:
    return re.sub(r"[^a-z0-9]+", "_", value.lower()).strip("_") or "span"
