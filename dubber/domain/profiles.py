from __future__ import annotations

import re
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any


@dataclass(frozen=True)
class ProtectedSpan:
    kind: str
    source_text: str
    canonical: str
    spoken: str
    display: str
    start_char: int
    end_char: int
    confidence: float
    rule_id: str
    forbidden: tuple[str, ...] = ()

    def to_dict(self) -> dict[str, object]:
        return {
            "kind": self.kind,
            "source_text": self.source_text,
            "canonical": self.canonical,
            "spoken": self.spoken,
            "display": self.display,
            "start_char": self.start_char,
            "end_char": self.end_char,
            "confidence": self.confidence,
            "rule_id": self.rule_id,
            "forbidden": list(self.forbidden),
        }


@dataclass(frozen=True)
class DomainProfile:
    profile_id: str
    version: str
    prompt_summary: str
    glossary_seeds: list[dict[str, object]] = field(default_factory=list)
    forbidden_translations: dict[str, list[str]] = field(default_factory=dict)

    @property
    def artifact_id(self) -> str:
        return f"{self.profile_id}@{self.version}"


_PROFILE_ALIASES = {
    "calculus": "calculus",
    "mathematics": "calculus",
    "math": "calculus",
    "educational math": "calculus",
}

_WORD_BOUNDARY = r"(?<![A-Za-zÀ-ỹ]){token}(?![A-Za-zÀ-ỹ])"


def load_domain_profile(domain: str, explicit_profile: str = "") -> DomainProfile:
    requested = (explicit_profile or domain or "general").strip().lower()
    profile_id = _PROFILE_ALIASES.get(requested, requested)
    if profile_id != "calculus":
        return DomainProfile(
            profile_id="generic",
            version="1",
            prompt_summary="Generic educational content. Preserve names, symbols, units, formulas, and numbers exactly when translation would change meaning.",
        )
    return _load_calculus_profile()


def detect_protected_spans(text: str, profile: DomainProfile) -> list[ProtectedSpan]:
    if profile.profile_id != "calculus":
        return []
    spans = _detect_calculus_spans(text)
    return _dedupe_spans(spans)


def glossary_terms_from_spans(
    spans: list[ProtectedSpan],
    *,
    source_segments: list[str],
    locked: bool,
) -> list[dict[str, object]]:
    terms: list[dict[str, object]] = []
    seen: set[str] = set()
    for span in spans:
        key = span.canonical.lower()
        if key in seen:
            continue
        seen.add(key)
        terms.append(
            {
                "term_id": f"protected_{_safe_id(span.canonical)}",
                "original": span.canonical,
                "vietnamese": span.spoken,
                "category": span.kind,
                "confidence": span.confidence,
                "locked": locked,
                "source_segments": source_segments,
                "notes": f"Domain protected span generated by {span.rule_id}.",
                "protected": True,
                "spoken": span.spoken,
                "display": span.display,
                "forbidden": list(span.forbidden),
            }
        )
    return terms


def normalize_spoken_text(display_text: str, protected_spans: list[ProtectedSpan | dict[str, object]]) -> str:
    text = display_text
    replacements: list[tuple[str, str]] = []
    for span in protected_spans:
        if isinstance(span, ProtectedSpan):
            canonical = span.canonical
            spoken = span.spoken
            display = span.display
        else:
            canonical = str(span.get("canonical", ""))
            spoken = str(span.get("spoken", ""))
            display = str(span.get("display", ""))
        if spoken:
            for original in (display, canonical):
                if original and original != spoken:
                    replacements.append((original, spoken))
    replacements.extend(
        [
            ("dy/dx", "d y trên d x"),
            ("d/dx", "d trên d x"),
            ("d/dt", "d trên d t"),
            ("πr²", "pi r bình phương"),
            ("π r²", "pi r bình phương"),
            ("½", "một phần hai"),
            ("×", " nhân "),
        ]
    )
    for original, spoken in sorted(replacements, key=lambda item: len(item[0]), reverse=True):
        if not original:
            continue
        text = re.sub(re.escape(original), spoken, text, flags=re.IGNORECASE)
    text = re.sub(r"(?<![A-Za-zÀ-ỹ])dr(?![A-Za-zÀ-ỹ])", "d r", text, flags=re.IGNORECASE)
    text = re.sub(r"(?<![A-Za-zÀ-ỹ])dx(?![A-Za-zÀ-ỹ])", "d x", text, flags=re.IGNORECASE)
    text = re.sub(r"(?<![A-Za-zÀ-ỹ])dy(?![A-Za-zÀ-ỹ])", "d y", text, flags=re.IGNORECASE)
    text = re.sub(r"(?<![A-Za-zÀ-ỹ])dt(?![A-Za-zÀ-ỹ])", "d t", text, flags=re.IGNORECASE)
    text = re.sub(r"([A-Za-zÀ-ỹ])²", r"\1 bình phương", text)
    text = re.sub(r"([A-Za-zÀ-ỹ])³", r"\1 lập phương", text)
    return re.sub(r"\s+", " ", text).strip()


def protected_spans_prompt(spans: list[ProtectedSpan | dict[str, object]]) -> list[dict[str, object]]:
    result: list[dict[str, object]] = []
    for span in spans:
        item = span.to_dict() if isinstance(span, ProtectedSpan) else dict(span)
        result.append(
            {
                "canonical": item.get("canonical", ""),
                "source_text": item.get("source_text", ""),
                "display": item.get("display", ""),
                "spoken": item.get("spoken", ""),
                "kind": item.get("kind", ""),
                "forbidden": item.get("forbidden", []),
            }
        )
    return result



def protected_translation_errors(
    source_text: str,
    translated_text: str,
    protected_spans: list[ProtectedSpan | dict[str, object]],
) -> list[str]:
    if not protected_spans:
        return []
    lower = translated_text.lower()
    spoken = normalize_spoken_text(translated_text, protected_spans).lower()
    errors: list[str] = []
    for span in protected_spans:
        item = span.to_dict() if isinstance(span, ProtectedSpan) else dict(span)
        canonical = str(item.get("canonical", "")).strip()
        expected_spoken = str(item.get("spoken", "")).strip().lower()
        forbidden = [str(value).lower() for value in item.get("forbidden", []) if str(value).strip()]
        for value in forbidden:
            if value in lower:
                errors.append(f"protected span {canonical} was translated as forbidden term {value}")
        if expected_spoken and expected_spoken not in spoken:
            errors.append(f"protected span {canonical} must be represented as {expected_spoken}")
    return errors

def _load_calculus_profile() -> DomainProfile:
    data = _load_yaml(Path(__file__).with_name("calculus.yaml"))
    return DomainProfile(
        profile_id=str(data.get("id", "calculus")),
        version=str(data.get("version", "1")),
        prompt_summary=str(data.get("prompt_summary", "")).strip(),
        glossary_seeds=list(data.get("glossary_seeds", [])),
        forbidden_translations={
            str(key): [str(item) for item in value]
            for key, value in dict(data.get("forbidden_translations", {})).items()
            if isinstance(value, list)
        },
    )


def _load_yaml(path: Path) -> dict[str, Any]:
    try:
        import yaml  # type: ignore[import-not-found]
    except ModuleNotFoundError:
        if path.name == "calculus.yaml":
            return _calculus_profile_fallback()
        raise

    loaded = yaml.safe_load(path.read_text(encoding="utf-8"))
    return loaded if isinstance(loaded, dict) else {}


